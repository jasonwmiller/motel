/// Bind to port 0 and return the OS-assigned port number.
pub fn get_available_port() -> u16 {
    let listener = TcpListener::bind("127.0.0.1:0").expect("failed to bind to port 0");
    listener.local_addr().unwrap().port()
}

// ---------------------------------------------------------------------------
// Server lifecycle
// ---------------------------------------------------------------------------

/// Start a motel server as a child process with the given ports.
/// Returns the `Child` handle so the caller can kill it when the test finishes.
pub fn start_server_process(grpc_port: u16, http_port: u16, query_port: u16) -> Child {
    let bin = env!("CARGO_BIN_EXE_motel");
    Command::new(bin)
        .args([
            "server",
            "--no-tui",
            "--grpc-addr",
            &format!("0.0.0.0:{grpc_port}"),
            "--http-addr",
            &format!("0.0.0.0:{http_port}"),
            "--query-addr",
            &format!("0.0.0.0:{query_port}"),
        ])
        .stdout(std::process::Stdio::null())
        .stderr(std::process::Stdio::null())
        .spawn()
        .expect("failed to start motel server")
}

/// Wait until the query gRPC port is accepting connections (up to ~5 seconds).
pub async fn wait_for_server(query_port: u16) {
    let addr = format!("127.0.0.1:{query_port}");
    for _ in 0..50 {
        if tokio::net::TcpStream::connect(&addr).await.is_ok() {
            // Give the server a moment to fully initialize after accepting TCP.
            tokio::time::sleep(Duration::from_millis(50)).await;
            return;
        }
        tokio::time::sleep(Duration::from_millis(100)).await;
    }
    panic!("server did not become ready on port {query_port} within 5 seconds");
}

/// Convenience: allocate three ports, start the server, wait for readiness.
/// Returns (grpc_port, http_port, query_port, child).
pub async fn start_test_server() -> (u16, u16, u16, Child) {
    let grpc_port = get_available_port();
    let http_port = get_available_port();
    let query_port = get_available_port();
    let child = start_server_process(grpc_port, http_port, query_port);
    wait_for_server(query_port).await;
    (grpc_port, http_port, query_port, child)
}

/// Guard that kills the child process when dropped.
pub struct ServerGuard {
    pub child: Child,
    pub grpc_port: u16,
    pub http_port: u16,
    pub query_port: u16,
}

impl ServerGuard {
    pub async fn start() -> Self {
        let (grpc_port, http_port, query_port, child) = start_test_server().await;
        Self {
            child,
            grpc_port,
            http_port,
            query_port,
        }
    }

    pub async fn start_with_args(extra_args: &[&str]) -> Self {
        let grpc_port = get_available_port();
        let http_port = get_available_port();
        let query_port = get_available_port();
        let bin = env!("CARGO_BIN_EXE_motel");
        let mut cmd = Command::new(bin);
        cmd.args([
            "server",
            "--no-tui",
            "--grpc-addr",
            &format!("0.0.0.0:{grpc_port}"),
            "--http-addr",
            &format!("0.0.0.0:{http_port}"),
            "--query-addr",
            &format!("0.0.0.0:{query_port}"),
        ]);
        for arg in extra_args {
            cmd.arg(arg);
        }
        let child = cmd
            .stdout(std::process::Stdio::null())
            .stderr(std::process::Stdio::null())
            .spawn()
            .expect("failed to start motel server");
        wait_for_server(query_port).await;
        Self {
            child,
            grpc_port,
            http_port,
            query_port,
        }
    }

    pub fn grpc_addr(&self) -> String {
        format!("http://127.0.0.1:{}", self.grpc_port)
    }

    pub fn http_base_url(&self) -> String {
        format!("http://127.0.0.1:{}", self.http_port)
    }

    pub fn query_addr(&self) -> String {
        format!("http://127.0.0.1:{}", self.query_port)
    }
}

impl Drop for ServerGuard {
    fn drop(&mut self) {
        let _ = self.child.kill();
        let _ = self.child.wait();
    }
}

