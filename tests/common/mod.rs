// Re-exported proto modules (generated by the crate's build.rs, available in
// OUT_DIR at build time). Integration tests share the same OUT_DIR when the
// crate is compiled, so tonic::include_proto! resolves the same generated code.
// If this does not work in your setup, an alternative is to add a [lib] target
// to Cargo.toml or duplicate the proto compilation in a test build script.
// ---------------------------------------------------------------------------

