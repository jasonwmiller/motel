// Shared test utilities for motel integration tests.
//
// Since motel is a binary crate, integration tests cannot import its internal
// modules directly. Instead we:
// 1. Start the motel server as a child process using `cargo bin`.
// 2. Re-generate proto types from the same .proto files using tonic::include_proto!.
// 3. Use tonic gRPC clients and reqwest HTTP clients to interact with the server.

use std::net::TcpListener;
use std::process::{Child, Command};
use std::time::Duration;

use prost::Message;

