/// Create a simple gauge metric for testing.
pub fn make_resource_metrics(metric_name: &str, value: f64) -> otel::metrics::v1::ResourceMetrics {
    use otel::common::v1::{AnyValue, KeyValue, any_value::Value};
    use otel::metrics::v1::{
        Gauge, Metric, NumberDataPoint, ResourceMetrics, ScopeMetrics, number_data_point,
    };
    use otel::resource::v1::Resource;

    ResourceMetrics {
        resource: Some(Resource {
            attributes: vec![KeyValue {
                key: "service.name".into(),
                value: Some(AnyValue {
                    value: Some(Value::StringValue("test-service".into())),
                }),
                ..Default::default()
            }],
            ..Default::default()
        }),
        scope_metrics: vec![ScopeMetrics {
            scope: None,
            metrics: vec![Metric {
                name: metric_name.to_string(),
                description: "test metric".to_string(),
                unit: "1".to_string(),
                data: Some(otel::metrics::v1::metric::Data::Gauge(Gauge {
                    data_points: vec![NumberDataPoint {
                        time_unix_nano: 1_000_000_000,
                        start_time_unix_nano: 0,
                        value: Some(number_data_point::Value::AsDouble(value)),
                        ..Default::default()
                    }],
                })),
                ..Default::default()
            }],
            ..Default::default()
        }],
        ..Default::default()
    }
}

/// Create an `ExportMetricsServiceRequest` ready to send.
pub fn make_export_metrics_request(
    metric_name: &str,
    value: f64,
) -> otel::collector::metrics::v1::ExportMetricsServiceRequest {
    otel::collector::metrics::v1::ExportMetricsServiceRequest {
        resource_metrics: vec![make_resource_metrics(metric_name, value)],
    }
}

/// Encode a prost Message to bytes (for HTTP protobuf bodies).
pub fn encode_proto<T: Message>(msg: &T) -> Vec<u8> {
    msg.encode_to_vec()
}
