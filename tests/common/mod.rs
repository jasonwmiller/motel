/// Create a protobuf-encoded `ExportTraceServiceRequest` containing a single
/// span with the given trace_id (16 bytes) and span_name.
pub fn make_resource_spans(trace_id: &[u8], span_name: &str) -> otel::trace::v1::ResourceSpans {
    use otel::common::v1::{AnyValue, KeyValue, any_value::Value};
    use otel::resource::v1::Resource;
    use otel::trace::v1::{ResourceSpans, ScopeSpans, Span};

    ResourceSpans {
        resource: Some(Resource {
            attributes: vec![KeyValue {
                key: "service.name".into(),
                value: Some(AnyValue {
                    value: Some(Value::StringValue("test-service".into())),
                }),
                ..Default::default()
            }],
            ..Default::default()
        }),
        scope_spans: vec![ScopeSpans {
            scope: None,
            spans: vec![Span {
                trace_id: trace_id.to_vec(),
                span_id: vec![1, 2, 3, 4, 5, 6, 7, 8],
                parent_span_id: vec![],
                name: span_name.to_string(),
                kind: 1, // SPAN_KIND_INTERNAL
                start_time_unix_nano: 1_000_000_000,
                end_time_unix_nano: 2_000_000_000,
                ..Default::default()
            }],
            ..Default::default()
        }],
        ..Default::default()
    }
}

/// Create an `ExportTraceServiceRequest` ready to send via gRPC or encode for HTTP.
pub fn make_export_trace_request(
    trace_id: &[u8],
    span_name: &str,
) -> otel::collector::trace::v1::ExportTraceServiceRequest {
    otel::collector::trace::v1::ExportTraceServiceRequest {
        resource_spans: vec![make_resource_spans(trace_id, span_name)],
    }
}

